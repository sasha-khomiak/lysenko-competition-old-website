      <p><br />
        On 22  March  2012 our country  celebrated the 170th Anniversary of Lysenko's birth.<br />
        <br />
Lysenko  was a composer, pianist-virtuoso, renowned choir conductor, leading figure of  national art education and organizer of some of the first All-Ukrainian social  organizations. Mykola Lysenko, the offspring of noble kin famous for the  incomparable representatives of Cossack elite of Bohdan Khmelnytsky times, was  fully devoted to the national idea, had a gift for educational activities and  possessed great music talent. In Ukraine he was at the front of artistic  development in the middle of 19th - at the beginning of 20th centuries.<br />
<br />
Influenced  by the folk song he got deeply aware of the heroic history of his native land,  devoted his talent to revive and consolidate Ukrainian people split between two  empires into one nation with its own state and culture.<br />
<br />
The composer made an outstanding contribution to the  national culture by collecting folk music treasures and reinstalling them as  exquisite artistic interpretations. Lysenko researched folk songs, arranged  them and revived music professional language. He arranged more than 500  Ukrainian folk  songs for choir, ensemble, voice and piano. Mykola Lysenko was one of the first  to study kobzar art and folk instruments.<br />
<br />
The  composer's mother, brilliant pianist, taught him his first music lessons.  From the family recollections we learn that his grandmother, father and many  closest relatives were gifted for music. Among teachers who gave the child lessons  there were K.Neinkevych, talented ensemblist Vilcek, well-known Kyiv pedagogue  and salon concert-player Aloiz Panocini (Ponotsny) and prominent Russian  musician Mykola Dmitriev.<br />
<br />
Successful  &quot;child&quot; debuts in Kharkiv and Kyiv inspired professional growth of  the young pianist. In 1861 Lysenko entered the natural sciences department of the  Kyiv University and in 1862- 63 took part in the City Theatre concert in favor of the  established Kyiv Branch of Emperor Russian Music Society. Concert-performer  Lysenko played Symphony No. 3 for piano and orchestra by Mendelssohn, Great  Polonaise by Weber, works by Liszt, Schumann and Beethoven, one of the most  difficult virtuoso transcriptions by Liszt - Paraphrase on &quot;Rigoletto&quot; by Verdi. No  wonder after graduating from the University and defending Candidate of Natural  sciences' thesis, the musician chose music forever instead of being a biology  lecturer at the Kyiv University.<br />
<br />
Lysenko  decided to get comprehensive professional music education and at the age of 25  became a student  at Leipzig Conservatoire. He finished education as a concert -pianist and  completed music theory - composition courses. He studied with the celebrated  professors of the Leipzig Conservatoire - I.Moscheles, K.Reineke, E.Wenzel and graduated performing  Piano Concerto No. 4 by Beethoven but with his own cadenza.<br />
<br />
After  returning to Kyiv in 1889 he started annual concerts which lasted till his  death, for nearly 40 years.<br />
A number  of instrumentatal and piano compositions written in those years in various  genres occupy an important place in Lysenko's heritage. Of great popularity was  even then his Piano Rhapsody No. 2 and Ukrainian Cossack- shumka as well as several piano  music miniatures and his famous Elegy in E Minor, nowadays a symbol of  Lysenko's epoch.<br />
<br />
