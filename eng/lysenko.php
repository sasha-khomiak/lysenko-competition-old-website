<br />
Lysenko  wrote more than 100 vocal and choir pieces on poems by Ukrainian poets.  One of the composer's vocal masterpieces was the cycle on Heine's poems, in  their first Ukrainian translation.<br />
<br />
Lysenko  was also one of the founders of the national theatre. He began writing music  for the theatre while being a student. His heritage includes music to 30  theatre  performances of various genres and 10 operas. Lysenkoís operas Christmas night (&quot;Rizdviana nich&quot;), &quot;Drowned† woman&quot;†  (´Utoplenaª),† &quot;Natalka  Poltavka&quot;, &quot;Eneida&quot; and &quot;Taras Bulba&quot; based on Gogol's story were staged  internationally. Lysenko's ability to write in various genres and skill to  combine folk tunes with European music tradition were of great value to  promote Ukrainian opera development. <br />
Worth of  mention was Lysenkoís active career as conductor and organizer of professional  choir in Ukraine.† In 1890-  1912 he toured the  country with above 60 choral concerts. <br />
<br />
Making  Ukraine part of world music education culture - in 1904 Lysenko opens the Kyiv  music- dramatic school that lay down the foundations of national composers' and  performers' schools. The composer adjusts academic syllabus of the school in  accordance with programmes of Moscow and St. Petersburg Conservatoires. <br />
<br />
Today, in  the days of the third millennium, looking closely at Lysenko's life and music  we can state that the composer's heritage became an integral part of the  national and world culture. There is an ever increasing interest of the new  generation musicologists to Lvsenko's works as well as, of course, musicians address his creative heritage on a daily basis. <br />
<br />
The  Lysenko International  Music Competition is a natural recognition of the composerís grandeur. It is  rather symbolical that the musician who became the founder of the national  composers' school gave name to the largest and most prestigious Ukrainian  competition. </p>
