      <h1>Competition History </h1>
      <p>&nbsp;</p>
      <p><br />
        <strong>The Lysenko Ñompetition</strong> was founded in 1962 by a   group of prominent Ukrainian   composers and performers: Andriy   Shtoharenko, Yevhen Stankovych, Myroslav   Skoryk, Yelysaveta Chavdar,   Yevhen Rzhanov and Ariadna Lysenko. It began as a   national music   competition and was held in such cities as Kyiv, Kharkiv, Lviv,   Odesa   and Zaporizhia. Over the 35 years of its existence the competition     developed into one of the most influential musical events in Ukraine. In   1992,   on the 150th anniversary of Lysenko's birth, it acquired   international status.   It is gratifying to note that several of   Ukraine's best-known and celebrated   performers have been winners of   previous Lysenko competitions.</p>
